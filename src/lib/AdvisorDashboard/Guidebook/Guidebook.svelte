    <h2>PeerConnect Handbook</h2>
    <p>Welcome to PeerConnect! The goal as an Advisor is to assist students whom you believe you can truly help. You may find this guide useful in helping other students-- thank you for participating!</p>
    <h3>Setting up your profile</h3>
    <p>On the left-hand side of the screen, you'll see a list of characteristics you can select. To more accurately be matched with those you relate to, select the ones that most accurately describe you.</p>
    <p>Don't worry, this information isn't shared with your peers or other Advisors.</p>
    <h3>Connecting to your first peer</h3>
    <p>In the center of the screen, incoming calls will appear once someone is waiting. If you click a peer, you can view more info about what they are requesting, such as what hobbies they are interested in or who they are interested in talking to. <b>It's best to connect to those closest to your area of expertise</b>. The "% match" on a peer will show you how much your interests align.</p>
    <p>Once you are ready to connect, click the "Connect" button. You will be connected to your peer, and you can begin talking.</p>
    <p></p>
    <h3>Conduct</h3>
    <p>As an Advisor, you represent your community and are responsible for giving help to those who need it. As such, you should always treat everyone with respect, regardless of the situation. An Advisor's job is to make sure their Peer feels welcomed and accepted!</p>
    <p>Peers can give feedback or report their Advisors after they end calls. Repeated misconduct may result in your Advisor account being banned, so don't do it!</p>
